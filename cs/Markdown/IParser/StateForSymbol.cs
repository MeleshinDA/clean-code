namespace Markdown
{
    /// <summary>
    /// Enum, в котором содержатся типы специальных символов, содержащиеся в тексте
    /// </summary>
    public enum SymbolType
    {
        Default,
        Underscore = '_',
        DoubleUnderscore,
        HashSymbol = '#',
        Slash = '/',
    }
}


